console.log('8 nagyobb, mint 4:', 8 > 4 );
console.log('8 kisebb, mint 4:', 8 < 4 );
console.log('8 kisebb vagy egyenlő, mint 4:', 8 <= 4 );
console.log('8 kisebb vagy egyenlő, mint 8:', 8 <= 8 );
console.log('8 egyenlő 8:', 8 == 8 );
console.log('8 egyenlő 6:', 8 == 6 );
console.log('8 nem egyenlő 8:', 8 != 8 );
console.log('8 nem egyenlő 6:', 8 != 6 );

// automatikus típuskonverzió conditional opreator esetében:
console.log('string egyenlő számmal:', '10' == 10);
// conditional operator-ok használata típuskonverzió nélkül:
console.log('string egyenlő számmal és megegyező típussal:', '10' === 10);
console.log('szám egyenlő számmal és megegyező típussal:', 10 === 10);
